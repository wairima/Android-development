fun main(args: Array<String>) {
    println("Number ranges")
    for (num in 2..10){
        println("The range numbers : $num")
}
    println("Year ranges")
    for (yr in 2000..2023){
        println(yr)
    }
    println("Letter ranges")
    for (letters in 'a'..'z'){
        println(letters)
    }
}